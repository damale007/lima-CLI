            <h2>La Leyenda de la Santa Cruz de la Calle Cabo</h2>

            <p>Se cuenta que se produjeron una serie de epidemias en el pueblo que obligaron a numerosas familias a evacuar sus hogares e irse al campo. Allí se asentaron como pudieron y una de ellas lo hizo a orillas de un arroyo. En una oscura y terrible noche tormentosa se ocasionó una gran tempestad produciéndose la avenida del agua del arroyo y llevándose todo lo que se encontraba por delante. Tuvo que marchar la familia apresuradamente para no ser arrastrada por el desbordamiento. Toda vez que cesó la tempestad y volvió la calma, la familia regresó de nuevo al lugar que había abandonado con anterioridad, encontrándose en el sitio justo donde tenían su cabaña dos leños cruzados formando estos una Cruz. Aquella familia lo vio como un milagro pues se salvaron de la catástrofe. Cuando pasó un tiempo y cesaron las epidemias, esta familia llegó al pueblo y al llegar a la calle Cabo le contaron a los vecinos lo ocurrido en el campo. Se dice que dicho Madero se encuentra dentro de la Cruz existente actualmente.</p>
