    <p>En 1992, el coro publicó su primer disco, titulado “Compañera” que contenía ocho temas, que estaban disponibles tanto en cassette como en discos de vinilo. El disco fue presentado en el Salón Nra. Sra. del Valle el 19 de diciembre de 1992.<br><br>
    Su segundo disco fue grabado al año siguiente, en 1993 y lleva por título Hoy con más fe que ayer. Este trabajo estaba disponible en los mismos formatos que el anterior, y tenía dos temas más. Fue presentado en el mismo salón que el anterior el 18 de diciembre de 1993.</p>
    <p>Hubo que esperar hasta diciembre del año 1994 para poder disfrutar de otro disco. Este titulado Sueños de primavera, que estaba disponible ya en CD. Este disco se presentó el 17 de diciembre de 1994, en el Salón Ntra. Sra. del Carmen.<br><br>
    Si bien hasta entonces salía un disco cada año, el cuarto se hizo más de esperar, el 4 de abril de 1997 se presentó en el Cine-Teatro España. El disco lleva por título A golpes de corazón y contaba con la novedad que incluía en Himno a la Santa Cruz de la Calle Cabo.</p>
    <p>El 16 de  mayo de 2025, se presentó en el Teatro España el quinto disco del coro, titulado "Poema", estando disponible en CD y Pen-drive</p>
    <p>Hasta la fecha no ha grabado ningún disco más, y tras haber celebrado en 2009 su 25 aniversario sigue actuando con la misma fe con que se creó en todos los actos que se requiere su presencia.</p>
