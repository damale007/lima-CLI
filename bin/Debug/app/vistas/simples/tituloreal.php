	    <h2 class="centrado">Credencial de concesión del título de Real</h2>

        <br><img src="../imagenes/Archivo/titulo de Real.jpg" class="imagenVertical">
        <br>
	    	<p>La Hermandad de la Santa Cruz de la Calle Cabo, en 2012 nombró a S.M. el Rey D. Juan Carlos I presidente de Honor del XXV Aniversario de sus primeras reglas, consiguiendo el 13 de mayo de 2021 el título de Real.</p>
            <p>Texto del documento:</p>
            <p>Su majestad el Rey, accediendo a la petición que tan amablemente le ha sido formulada, ha tenido a bien conceder el título de Real a la Fervorosa Hermandad de la Santa Cruz, Santa Caridad y Nuestra Señora del Rosario (Calle Cabo), de La Palma del Condado (Huelva).</p>
            <p>Lo que me complace participarle para su conocimiento y efectos.</p>
            <p>Palacio de la Zarzuela, 13 de mayo de 2021</p>

