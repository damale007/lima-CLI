                <h2>La Santa Cruz de la Calle Cabo</h2>
                <p>
                    Cruz en madera, revestida con un traje bordado en hilos de plata engarzados con oro. Su altura desde la base hasta el INRI es de 1,30 metros. El vestido se diferencia en dos partes: una delantera realizada en al año 1925 por D. Juan Manuel Rodríguez Ojeda. En esta parte se encuentra un Cáliz realizado en madera y recubierto con hilos de oro, sobre éste se encuentra la Forma realizada en plata y bordado en oro con la inscripción: JHS; debajo del Cáliz se encuentra el Lignum Crucis incluido en un rosetón con un viril, ambos realizados en oro. En los lugares donde estuvieron clavadas las manos y los pies de Ntro. Señor posee tres clavos de oro. La parte trasera es más antigua que la delantera, mediados del siglo XIX aproximadamente, de las mismas características que la anterior. En la parte inferior se completa con un trozo del antiguo traje, siglo XVII. (Por lo que se supone que en este siglo ya se vestía la Santa Cruz). En el centro del crucero existe una foto del rostro del Señor rodeado por una corona de espinas labrada en oro.
                </p>
                <p>
                    La Santa Cruz está rodeada por un cordón dorado, y tiene cuatro espigas realizadas en lentejuelas doradas.
                </p>
                <p>
                    No se conoce autor ni fecha de creación de la Santa Cruz de la Calle Cabo. El primer documento sobre la Santa Cruz es una factura del pago del traje de la parte delantera, obra de D. Juan Manuel Rodríguez Ojeda. El traje que poseía en la parte delantera pasa a la parte trasera, donde actualmente permanece. Es el traje llamado "de las rosas". Se cree que data del siglo XIX, aunque cuando Rodríguez Ojeda crea el traje para la parte delantera, limpia el tisú de "las rosas" y puede que añada algún elemento.
                </p>
