                    La Hermandad cuenta con varios fragmentos de anteriores trajes, siendo el más antiguo constituido por un tejido base de seda con hilo metálico de los denominados «lamé», con un bordado plano con motivos vegetales y florales. D. Gregorio Ferrreras, Técnico del Instituto de Patrimonio Andaluz, ha realizado un minucioso examen del trozo de tela bordada, ofreciendo la siguiente conclusión:
                </p>

                <p>
                    Por las características o connotaciones morfológicas del fragmento del textil, basado en los tejidos que se utilizaban para los ternos y enseres litúrgicos de la época, de gran influencia francesa denominados de tipo «lamé» o «tisú». Por el tipo de diseño floral que presenta que es propio también de ese momento, si lo comparamos estilísticamente con otros tejidos que están documentados de esa época. Pequeños motivos florales, repetidos y siguiendo un diseño simétrico. Y por la aleación y porcentajes del hilo metálico de plata que se utiliza en esta obra y que comparándola con otras piezas analizadas de la época presentan las mismas características y porcentajes.»
                </p>

                <p>
                    Más recientemente, el trozo del traje ha sido revisado por D. Antonio Mañes Manaute (historiador del arte y uno de los mejores especialistas en la artesanía del bordado de España), quien lo ha datado de mitad del siglo XVII, ya que ha encontrado un patrón igual en algunos bordados de Sevilla de esa misma fecha.
                </p>
