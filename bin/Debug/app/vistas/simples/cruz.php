                <p>
                    En el principio del cristianismo cuando la cruz era un signo de ignominia y la dominación romana seguía vigente, los cristianos tomaron como signo de identificación, el Pez dado que en sus letras griegas iba el monograma de "Jesús Hombre Salvador del Mundo", más tarde, sobre el siglo VI, el cristianismo adoptó la Cruz como símbolo de su Fe.
                </p>
                <p>
                    Fue en el siglo IV cuando la cruz se convirtió, poco a poco, en el símbolo predilecto para representar a Cristo y su misterio de salvación. En octubre del año 312, justo antes de la Batalla del Puente Milvio, según la leyenda una Cruz se le apareció en sueños al <b>Emperador Constantino I</b>, pidiéndole que sustituyera las águilas imperiales de las insignias de los soldados por la Cruz cristiana, de manera que con ese signo vencería. Tras la victoria el Emperador Constantino se convierte al Cristianismo.</p>
                    
                <p>
                    Posteriormente, su madre, <b>Santa Elena</b> peregrinó a Tierra Santa para buscar la reliquia de la Vera Cruz (la auténtica cruz de Cristo). En su búsqueda de la cruz donde Jesucristo murió, demolió el templo erigido a Venus en el monte Calvario e hizo cavar hasta que le dieron noticias, en los primeros días de mayo, de haber encontrado la Cruz. Elena mandó construir un templo allí y otro en el monte de los Olivos. 
                </p>
                <p>
                    Las primeras representaciones pictóricas o esculturales de la Cruz ofrecen a un Cristo Glorioso, con larga túnica, con corona real: está en la Cruz, pero es el Vencedor, el Resucitado. Sólo más tarde, con la espiritualidad de la Edad Media, se le representará en su estado de sufrimiento y dolor.
                </p>
