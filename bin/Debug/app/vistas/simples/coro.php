<section class="documento">
    <p>Comenzaba la década de los años ochenta del pasado siglo con nuevas inquietudes en la hermandad, un nutrido grupo de jóvenes comenzaban a acercarse a la “secretaría” para colaborar con los más veteranos.</p>
    <p>De esas inquietudes surge el Coro de la Hermandad de la Santa Cruz de la Calle Cabo se creó en septiembre de 1984. Un grupo de jóvenes movidos por el Sacerdote Salesiano D. Miguel Moreno Gutiérrez, se unieron con el objetivo de cantar en los actos y cultos que se celebraban en honor de la Santa Cruz, como el Triduo, la Función o durante el Santo Rosario el viernes de las Fiestas.</p>
    <p>Se comenzó ensayando en la Capilla de la Santa Cruz de la Calle Cabo los domingos por la tarde y adaptando letras de otros grupos flamencos a la Santa Cruz, cantando por primera vez en el Triduo y Función del año 1985.</p>
    <p>Su obra musical es abundante y rica, su aportación a las coplas y sevillanas populares de la tradición crucera palmerina, llegándose a componer más de 100 letras. Ha tenido multitud de actuaciones tanto en nuestro pueblo como en otras localidades, no solo de la comarca sino de otras comunidades Autónomas como Extremadura, y tiene grabado cuatro discos en los que se recogen temas a la Virgen del Valle, Virgen del Rocío, al pueblo de La Palma y como no, principalmente a la Santa Cruz de la Calle Cabo.</p>
    <br><br>

